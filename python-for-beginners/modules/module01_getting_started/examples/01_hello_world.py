"""
Module 01 — Example 01: Hello World

Demonstrates:
  - The classic first program every programmer writes
  - print() with a single string argument
  - print() with multiple arguments
  - How Python puts spaces between multiple arguments automatically

Run this file:
  python modules/module01_getting_started/examples/01_hello_world.py
"""

# The most famous line in programming history
print("Hello, world!")

# print() with multiple arguments — Python separates them with spaces
print("Hello,", "world!")

# You can print numbers too — no quotes needed for numbers
print("The answer is", 42)

# You can print a blank line by calling print() with no arguments
print()

# A more personal greeting
print("Welcome to Python for Beginners!")
print("You are about to learn one of the world's most popular programming languages.")
print()
print("Let's get started.")
