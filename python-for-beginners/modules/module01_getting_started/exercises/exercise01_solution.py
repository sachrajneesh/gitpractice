# A header to make the output look organised
print("=== About Me ===")

# Blank line for spacing
print()

# Name — using multiple arguments so print() adds the space automatically
print("Name:", "Alex Smith")

# Age — same approach
print("Age:", 17)

# Blank line before the quote
print()

# Favourite quote — using single quotes on the outside so we can use
# double quotes inside without escaping
print('Favourite quote: "The expert in anything was once a beginner."')

# Blank line at the end
print()

# A closing line — just for fun
print("Nice to meet you!")

